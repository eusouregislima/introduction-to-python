# Inteiro
numero_inteiro = 5
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.15
print("Real com número flutuante", numero_real)

# type()
print("Tipo da variável inteiro =", type(numero_inteiro))
print("Tipo da variável real =", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 =", soma)

# subtração -
subtracao = 1 - 1
print("1 - 1 =", subtracao)

#multiplicação *
multiplicacao = 2 * 2
print("2 x 2 =", multiplicacao)

# divisão /
divisao = 5 / 2
print("5 / 2 =", divisao)
print("Tipo da variável do resultado = ", type(divisao))
# Sempre que fizer uma divisão o resultado será float, para converter usa a função int()
print("Valor em inteiro = ", int(divisao)) # Saída 2

# convertendo para float
print("Valor em float = ", float(soma)) # Saída 2.0

#Módulo %
modulo = 5 % 2
print("O resto da divisão de 5 por 2 é igual a", modulo) # Saída 1

# divisão inteiro //
divisao_inteiro = 5 // 2
print("5 // 2 =", divisao_inteiro)
print("Resultado da divisão em inteiro = ", divisao_inteiro) # Saída 2